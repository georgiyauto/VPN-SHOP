def get_flag(server_label: str) -> tuple[str, str]:
    """
    По названию сервера возвращает (флаг, страна).
    Пример: get_flag("germany-1") → ("🇩🇪", "Германия")
    """
    label_lower = server_label.lower().replace("-", "_").replace(" ", "_")

    # Точное совпадение или вхождение ключевого слова
    for keyword, (flag, country) in COUNTRY_FLAGS.items():
        if keyword in label_lower:
            return flag, country

    return "🌍", "Unknown"


def label_with_flag(server_label: str) -> str:
    """
    Добавляет флаг к названию если его ещё нет.
    Пример: "Germany 1" → "🇩🇪 Germany 1"
    """
    # Если уже есть флаг-эмодзи — не добавляем
    if server_label and len(server_label) > 0:
        first_char = server_label[0]
        # Региональные индикаторы — это эмодзи флаги (U+1F1E6–U+1F1FF)
        if '\U0001F1E6' <= first_char <= '\U0001F1FF':
            return server_label
        # Другие флаги/эмодзи в начале
        if ord(first_char) > 127:
            return server_label

    flag, _ = get_flag(server_label)
    if flag != "🌍":
        return f"{flag} {server_label}"
    return server_label
